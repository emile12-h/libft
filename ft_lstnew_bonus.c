#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstnew(void *content)
{
	t_list	*new_element;

	new_element = (t_list *)malloc(sizeof(t_list));
	if (!new_element)
		return ((void *) '\0');
	new_element->content = content;
	new_element->next = (void *) '\0';
	return (new_element);
}
