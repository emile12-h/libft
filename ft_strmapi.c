#include "libft.h"
#include <stdlib.h>

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*result;

	i = 0;
	result = (void *) '\0';
	if (!s)
		return ((void *) '\0');
	result = (char *)malloc(ft_strlen(s) + 1);
	if (!result)
		return ((void *) '\0');
	while (s[i] != '\0')
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
